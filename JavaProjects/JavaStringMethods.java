public class Review {
  
  public static void main(String[] args) {
    String = myInfo = "An average man with an average mind.";
    String = extraInfo = "I like to develop apps.";

    System.out.println(myInfo.length());
    System.out.println(myInfo.concat(extraInfo));
    System.out.println(myInfo.indexOf(0));
    System.out.println(myInfo.charAt(1));
    System.out.println(myInfo.equals(extraInfo));
    System.out.println(myInfo.substring(2, 9));
    System.out.println(extraInfo.toUpperCase());
    
    
  }
  
}
